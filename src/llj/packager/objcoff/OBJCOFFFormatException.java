package llj.packager.objcoff;

public class OBJCOFFFormatException extends Exception {

    public OBJCOFFFormatException(String message) {
        super(message);
    }

    public OBJCOFFFormatException(String message, Throwable cause) {
        super(message, cause);
    }

    public OBJCOFFFormatException(Throwable cause) {
        super(cause);
    }
}
